    private void kirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kirimActionPerformed
        // TODO add your handling code here:
         try{
            String sql = "SELECT * FROM pegawai Where username='" + username.getText() + 
            "'AND password='" + pass.getText()+"'";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery(sql);
            
            boolean cekNim = username.getText().equals(rs.getString("username"));
            boolean cekPass = pass.getText().equals((rs.getString("password")));

            if(rs.next()){
                if( cekNim && cekPass ){
                        JOptionPane.showMessageDialog(null, "Berhasil login");
                        this.setVisible(false);
                        new main.menu_utama().setVisible(true);
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Username atau password salah");
        }                        
    }//GEN-LAST:event_kirimActionPerformed

