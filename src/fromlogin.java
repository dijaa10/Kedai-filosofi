    private void passKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
           try{
            String sql = "SELECT * FROM pegawai Where username='" + username.getText() + 
            "'AND password='" + pass.getText()+"'";
            java.sql.Connection conn = (Connection)config.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
                if(username.getText().equals(rs.getString("username"))
                        && pass.getText().equals((rs.getString("password")))){
                        JOptionPane.showMessageDialog(null, "Berhasil login");
                        this.setVisible(false);
                        new main.menu_utama().setVisible(true);;
                }
            }else{
                JOptionPane.showMessageDialog(null, "Username atau password salah");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }                
       }
    }//GEN-LAST:event_passKeyPressed

